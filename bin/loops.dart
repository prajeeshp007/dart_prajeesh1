void main(){
  ///for loop
  ///syntax = for(initialization; condition ; increment/decrement counter){code to be executed}
  for(int i = 1; i<= 10;i++){
    print(i);

  }

  ///while loop
  ///syntax initialization;
  // while(condition){
  // code to be executed
  //increment/decrement counter;
  // }
  int i = 9;
  while(i>=0){
    print(i);
    i--;
  }




  ///do while
  // initialization;
  //do{
  // code to be executed;
  //increment/decrement counter;
  // }while(condition);
  int num =5;
  do{
    print(num);
    num++;
  }while(num<=10);



}